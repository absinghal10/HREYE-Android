package cbs.hreye.pojo;

public class PaySlipDetailsPojo {
    private String Amount;
    private String Earning;
    private String GrossAmt;
    private String Deduction;
    private String DeductionAmt;

    public String getAmount() {
        return Amount;
    }

    public void setAmount(String amount) {
        Amount = amount;
    }

    public String getEarning() {
        return Earning;
    }

    public void setEarning(String earning) {
        Earning = earning;
    }

    public String getGrossAmt() {
        return GrossAmt;
    }

    public void setGrossAmt(String grossAmt) {
        GrossAmt = grossAmt;
    }


    public String getDeduction() {
        return Deduction;
    }

    public void setDeduction(String deduction) {
        Deduction = deduction;
    }

    public String getDeductionAmt() {
        return DeductionAmt;
    }

    public void setDeductionAmt(String deductionAmt) {
        DeductionAmt = deductionAmt;
    }
}
