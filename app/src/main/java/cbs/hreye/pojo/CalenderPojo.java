package cbs.hreye.pojo;

public class CalenderPojo {

    private String CALENDER_CODE;
    private String COLOR_CODE;
    private String DATE;
    private String HOLI_CAL;
    private String OCCASION;

    public String getCALENDER_CODE() {
        return CALENDER_CODE;
    }

    public void setCALENDER_CODE(String CALENDER_CODE) {
        this.CALENDER_CODE = CALENDER_CODE;
    }

    public String getCOLOR_CODE() {
        return COLOR_CODE;
    }

    public void setCOLOR_CODE(String COLOR_CODE) {
        this.COLOR_CODE = COLOR_CODE;
    }

    public String getDATE() {
        return DATE;
    }

    public void setDATE(String DATE) {
        this.DATE = DATE;
    }

    public String getHOLI_CAL() {
        return HOLI_CAL;
    }

    public void setHOLI_CAL(String HOLI_CAL) {
        this.HOLI_CAL = HOLI_CAL;
    }

    public String getOCCASION() {
        return OCCASION;
    }

    public void setOCCASION(String OCCASION) {
        this.OCCASION = OCCASION;
    }
}
